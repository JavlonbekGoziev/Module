

function add (a, b) {
    return a + b
}

function minus(a,b) {
    return a - b
}

function multiply (a, b) {
    return a * b
}

function division(a,b) {
    return a / b
}

function persentage (a, b) {
    return a % b
}

function combined(a,b,d) {
    return a - b + d
}

module.exports = {
    add, minus, multiply, division, persentage, combined
}